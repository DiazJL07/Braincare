// 4. Función para actualizar todo el contenido traducible
function updateAllContent() {
    // Actualizar textos normales
    document.querySelectorAll('[data-i18n]').forEach(el => {
        try {
            const key = el.getAttribute('data-i18n');
            el.textContent = i18next.t(key);
        } catch (e) {
            console.warn('Error translating element:', el, e);
        }
    });
    
    // Actualizar placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = i18next.t(el.getAttribute('data-i18n-placeholder'));
    });
    
    // Actualizar valores de inputs/buttons
    document.querySelectorAll('[data-i18n-value]').forEach(el => {
        el.value = i18next.t(el.getAttribute('data-i18n-value'));
    });
    
    // Actualizar atributos alt para imágenes (opcional)
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        el.alt = i18next.t(el.getAttribute('data-i18n-alt'));
    });
}

// 5. Asegurarse de que el idioma se mantenga al cargar nuevas páginas
document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('selectedLanguage');
    if (savedLang && savedLang !== i18next.language) {
        i18next.changeLanguage(savedLang).then(updateAllContent);
    }
});

